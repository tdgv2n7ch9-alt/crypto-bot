def find_lesson(key: str):
    """Возвращает (module, lesson) по ключу урока, или (None, None), если не
    найден -- честно, не выдумывает."""
    for module in MODULES:
        for lesson in module["lessons"]:
            if lesson["key"] == key:
                return module, lesson
    return None, None


def find_module(module_id: int):
    for module in MODULES:
        if module["id"] == module_id:
            return module
    return None


def total_lesson_count() -> int:
    return sum(len(m["lessons"]) for m in MODULES)
