"""
glossary.py -- Карточка v2 / Меню v2 (Пакет 13), «словарь-в-строке» + /терминология.
Контент взят из уже существующего knowledge/GLOSSARY.md (написан ранее в проекте,
никогда не был кодом -- см. разведку Пакета 13), здесь только код-обёртка:
термин при ПЕРВОМ появлении в карточке получает расшифровку в скобках, задаётся
явным вызовом на месте использования (не автоматический скан текста -- проще,
безопаснее, не путает термин с частью другого слова).
"""

TERMS = {
    "R:R": "соотношение прибыль/риск",
    "DCA": "усреднение позиции частями",
    "SL": "стоп-лосс, уровень принудительного закрытия при убытке",
    "TP": "тейк-профит, уровень частичной/полной фиксации прибыли",
    "OI": "открытый интерес, сумма непогашенных фьючерсных контрактов",
    "funding": "периодическая выплата между лонгами и шортами на перпетуалах",
    "killzone": "окно повышенной ликвидности/волатильности по ICT-методологии",
    "лимитка": "лимитный ордер, вход по заданной цене, а не по рынку",
    "безубыток": "перенос стопа на цену входа после частичной фиксации прибыли",
}


def term_note(term: str) -> str:
    """Расшифровка термина в скобках для первого употребления в тексте карточки.
    Пустая строка для неизвестного термина -- не выдумывает объяснение."""
    definition = TERMS.get(term)
    return f" ({definition})" if definition else ""


def annotate(term: str) -> str:
    """Термин + расшифровка одной строкой -- то, что подставляется в карточку
    при первом употреблении: `glossary.annotate("R:R")` -> "R:R (соотношение
    прибыль/риск)"."""
    return f"{term}{term_note(term)}"


def format_glossary_text() -> str:
    """Текст для команды /терминология -- полный список терминов по алфавиту."""
    lines = ["📖 *Терминология*", ""]
    for term in sorted(TERMS.keys(), key=str.lower):
        lines.append(f"*{term}* -- {TERMS[term]}")
    return "\n".join(lines)
