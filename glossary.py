# ПАКЕТ UX-НАВИГАЦИЯ, п.2 (владелец): "[❓ Словарь] на карточках с терминами --
# вызов glossary с краткой расшифровкой ИМЕННО терминов этой карточки", не
# полный /терминология список. Порядок -- порядок появления термина на карточке.
CARD_TERMS = {
    "whale": ["funding", "OI", "L/S", "Rocket Score", "грейд", "звёзды"],
    "tochki": ["светофор", "Rocket Score", "грейд", "R:R", "SL", "TP"],
    "pump": ["Z-Score", "OI", "funding", "DCA"],
    "x100": ["Rocket Score", "грейд", "R:R"],
}
CARD_TITLES = {
    "whale": "🐋 Whale Radar",
    "tochki": "🎯 ТОЧКИ",
    "pump": "⚡ Памп-радар",
    "x100": "🚀 x100",
}


def term_note(term: str) -> str:
    """Расшифровка термина в скобках для первого употребления в тексте карточки.
    Пустая строка для неизвестного термина -- не выдумывает объяснение."""
    definition = TERMS.get(term)
    return f" ({definition})" if definition else ""


def annotate(term: str) -> str:
    """Термин + расшифровка одной строкой -- то, что подставляется в карточку
    при первом употреблении: `glossary.annotate("R:R")` -> "R:R (соотношение
    прибыль/риск)"."""
    return f"{term}{term_note(term)}"


def format_glossary_text() -> str:
    """Текст для команды /терминология -- полный список терминов по алфавиту."""
    lines = ["📖 *Терминология*", ""]
    for term in sorted(TERMS.keys(), key=str.lower):
        lines.append(f"*{term}* -- {TERMS[term]}")
    return "\n".join(lines)


def format_card_glossary_text(card: str) -> str:
    """ПАКЕТ UX-НАВИГАЦИЯ, п.2 (владелец): текст для кнопки [❓ Словарь] на
    конкретной карточке -- ТОЛЬКО термины этой карточки (`CARD_TERMS[card]`),
    не полный /терминология список. `card` не из `CARD_TERMS` -- честное
    "н/д", не выдумывает список."""
    terms = CARD_TERMS.get(card)
    title = CARD_TITLES.get(card, card)
    if not terms:
        return f"📖 *Словарь -- {title}*\n\nн/д (список терминов не задан для этой карточки)."
    lines = [f"📖 *Словарь -- {title}*", ""]
    for term in terms:
        definition = TERMS.get(term)
        if definition:
            lines.append(f"*{term}* -- {definition}")
    return "\n".join(lines)
